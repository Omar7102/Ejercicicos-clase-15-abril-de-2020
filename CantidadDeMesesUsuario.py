#Cantidad de meses transcurridos desde la fecha de nacimiento de un usuario.
dia=int(input('Digite el dia de su nacimineto\n'))
mes=int(input('Digite el numero del mes de su nacimineto\n'))
año=int(input('Digite el año de su nacimineto\n'))
edad=int(input('Digite su edad\n'))
meses=edad*12
print('Su fecha de nacimiento es:', dia, '/', mes, '/', año)
print ('usted tiene', meses, 'meses de vida') 
